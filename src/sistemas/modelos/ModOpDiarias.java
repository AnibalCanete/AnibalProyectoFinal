
package sistemas.modelos;

//Tabla Operaciones diarias

public class ModOpDiarias {
    private String revisiones;
    private String reportes;
    private String recepcion;
    private String envios;
    private String descripcion;

    public String getRevisiones() {
        return revisiones;
    }

    public void setRevisiones(String revisiones) {
        this.revisiones = revisiones;
    }

    public String getReportes() {
        return reportes;
    }

    public void setReportes(String reportes) {
        this.reportes = reportes;
    }

    public String getRecepcion() {
        return recepcion;
    }

    public void setRecepcion(String recepcion) {
        this.recepcion = recepcion;
    }

    public String getEnvios() {
        return envios;
    }

    public void setEnvios(String envios) {
        this.envios = envios;
    }

    public String getDescripcion() {
        return descripcion;
    }

    public void setDescripcion(String descripcion) {
        this.descripcion = descripcion;
    }
    
            
}
