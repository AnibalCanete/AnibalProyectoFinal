package sistemas.modelos;

//Tabla Contabilidad

public class ModContabilidad {
    private String informes;
    private String presupuestos;
    private String descripcion;

    public String getInformes() {
        return informes;
    }

    public void setInformes(String informes) {
        this.informes = informes;
    }

    public String getPresupuestos() {
        return presupuestos;
    }

    public void setPresupuestos(String presupuestos) {
        this.presupuestos = presupuestos;
    }

    public String getDescripcion() {
        return descripcion;
    }

    public void setDescripcion(String descripcion) {
        this.descripcion = descripcion;
    }

    
}
